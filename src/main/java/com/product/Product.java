package com.product;

public class Product {
	  
	private int id;    
	private String name;    
	private int price;    
	private String date;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int f) {
		this.price = f;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}    
	    
	
	    
	}    

